'''
Escreva um programa que escreve na tela de 1 até 100, de 1 em 1 vez, 2 vezes. A primeira deve usar
a estrutura de repetição for, e a segunda, while.
'''

# Usando o for
for n in range(1, 101):
    print(n, end=' ')

# Usando o while
n = 1
while n <= 100:
    print(n, end=' ')
    n = n + 1  # Isso garante que o n vai chegar até 100 e 

